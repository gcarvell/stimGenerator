
def mapThis(stim, condition, sameOnTheLeft):

	values = []
	for s in stim:
		values.append(s[2])

	mapped = []
	if condition == "difference":
		leastSimilar = max(values)
		for value in values:
			mappedValue = value/leastSimilar
			if sameOnTheLeft:
				mapped.append(mappedValue)
			else:
				mapped.append(1-mappedValue)

	elif condition == "ratio":
		for value in values:
			mappedValue = value
			if sameOnTheLeft:
				mapped.append(1-mappedValue)
			else:
				mapped.append(mappedValue)


	else:
		print("Problem with condition: {}".format(condition))

	for i, s in enumerate(stim):
		s.append(mapped[i])

	return stim

